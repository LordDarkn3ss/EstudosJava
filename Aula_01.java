public class Aula_01 {
	
	
	
	public static void main(String[] args) {
		
		int a = 10;
		int b = 10;
		
		System.out.println(a++ + ++a); // var++ adiciona depois de passar pela variavel
		System.out.println(b++ + b++); // ++var adiciona antes de passar pela variavel
		
	
	}	
	
}
