package Fundamentos;

public class OperadorExemplos {
	public static void main(String[] args) {
		int a = 10;
		int b = -10;
		boolean c = true;
		boolean d  = false;
		System.out.println(~a); // inverte e tira 1
		System.out.println(~b); // inverte e tira 1
		System.out.println(!c); //false (inverte o valor bool)
		System.out.println(!d); //true (inverte o valor bool)
		
	}

}
